describe('makeRandomRS256', () => {
  it('creates a RS256 signer and verifier', () => {
    const key = makeRandomRS256('my-key-id');
    expect(key.kid).equals('my-key-id');
    expect(key.alg).equals('RS256');
    expect(key.sign).isTruthy();
    expect(key.verify).isTruthy();
  });
});

