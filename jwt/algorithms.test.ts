describe('algorithms', () => {
  it(
    'signs',
    ({ key, data, signature }: any) => {
      expect(key.sign(data).toString('base64url')).equals(signature);
    },
    { parameters: TESTS },
  );

  it(
    'verifies',
    ({ key, data, signature, incorrectSignature }: any) => {
      expect(key.verify(data, Buffer.from(signature, 'base64url'))).isTrue();
      expect(key.verify(data, Buffer.from(incorrectSignature, 'base64url'))).isFalse();
    },
    { parameters: TESTS },
  );
});

