function readSampleHMACKey(key: string) {
  return {
    kid: undefined,
    key: createSecretKey(key, 'base64url'),
  };
}

function readSampleRSAKey(key: JsonWebKey) {
  return {
    kid: undefined,
    publicKey: createPublicKey({ format: 'jwk', key }),
    privateKey: createPrivateKey({ format: 'jwk', key }),
  };
}
